class User {
    constructor(fn, ln, add, dob, dov, g, r, em, c, t, user, pass) {
        this.firstname = fn;
        this.lastname  = ln;
        this.address   = add;
        this.dateob    = dob;
        this.daysov    = dov;
        this.gender    = g;
        this.role      = r;
        this.email     = em;
        this.contact   = c;
        this.team      = t;
        this.username  = user;
        this.password  = pass;       
  }
}